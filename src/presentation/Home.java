    public void disposeCurrentFrame(){
         Timer timer = new Timer(500, new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            // Dispose the current frame  after the delay
            dispose();}
    });
    // Start the timer
    timer.setRepeats(false);
    timer.start();
    }
